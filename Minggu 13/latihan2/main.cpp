#include <iostream>

using namespace std;

int main()
{
    /**
    Mencari Nilai Tertinggi dan Terendah
    **/

    int a, b, c, tertinggi, terendah;

    cout << "Masukkan Nilai a : "; cin >> a;
    cout << "Masukkan Nilai b : "; cin >> b;
    cout << "Masukkan Nilai c : "; cin >> c; cout << endl;

    tertinggi = 0;
    terendah < tertinggi;

    if (tertinggi < a) {tertinggi = a;}
    if (tertinggi < b) {tertinggi = b;}
    if (tertinggi < c) {tertinggi = c;}

    if (terendah > a) {terendah = a;}
    if (terendah > b) {terendah = b;}
    if (terendah > c) {terendah = c;}

    cout << "Tertinggi = " << tertinggi << endl << "Terendah = " << terendah;

    return 0;
}
